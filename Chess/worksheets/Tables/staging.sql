create table staging(
    `EVENT` VARCHAR(255),
    `SITE` VARCHAR(255),
    `START_DATE` DATE,
    `ROUND` VARCHAR(16),
    WHITE VARCHAR(32),
    BLACK VARCHAR(32),
    RESULT VARCHAR(8),
    CURRENT_POSITION VARCHAR(255),
    TIMEZONE VARCHAR(64),
    ECO VARCHAR(64),
    ECO_URL VARCHAR(255),
    `UTC_DATE` DATE,
    `UTC_TIME` VARCHAR(16),
    WHITE_ELO VARCHAR(8),
    BLACK_ELO VARCHAR(8),
    TIME_CONTROL VARCHAR(32),
    TERMINATION VARCHAR(128),
    START_TIME VARCHAR(16),
    END_DATE DATE,
    END_TIME VARCHAR(16),
    LINK VARCHAR(255),
    MAINLINE TEXT,
    TOURNAMENT VARCHAR(255),
    VARIANT VARCHAR(255),
    FEN VARCHAR(255),
    SETUP VARCHAR(255),
    `MATCH` VARCHAR(255),
    GAME_HASH VARCHAR(64)
)
ENGINE = InnoDB,
CHARACTER SET = 'utf8mb4',
COLLATE = 'utf8mb4_general_ci'
;